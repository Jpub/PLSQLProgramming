SET ECHO ON
SET TAB OFF
SET SERVEROUTPUT ON

COL 소스코드 FORMAT A79

REM ALL_SOURCE에서 저장 서브프로그램 소스코드 조회
SELECT TEXT AS 소스코드
  FROM ALL_SOURCE
 WHERE OWNER = 'SCOTT'
   AND NAME = 'LOG_MSG'
 ORDER BY LINE;

PAUSE

REM USER_SOURCE에서 저장 서브프로그램 소스코드 조회
SELECT TEXT AS 소스코드
  FROM USER_SOURCE
 WHERE NAME = 'LOG_MSG'
 ORDER BY LINE;

PAUSE

REM 권한이 있다면 DBA_SOURCE에서 저장 서브프로그램 소스코드 조회
SELECT TEXT AS 소스코드
  FROM DBA_SOURCE
 WHERE OWNER = 'SCOTT'
   AND NAME = 'LOG_MSG'
 ORDER BY LINE;
