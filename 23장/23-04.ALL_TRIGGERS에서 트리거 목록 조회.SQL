SET ECHO ON
SET TAB OFF
SET SERVEROUTPUT ON

COL TABLE_OWNER  FORMAT A10
COL TABLE_NAME   FORMAT A30
COL TRIGGER_NAME FORMAT A30

REM ALL_TRIGGERS에서 트리거 목록 조회
SELECT TABLE_OWNER, TABLE_NAME, TRIGGER_NAME
  FROM ALL_TRIGGERS
 WHERE TABLE_OWNER = 'SCOTT'
 ORDER BY 1,2,3;

PAUSE

REM USER_TRIGGERS에서 트리거 목록 조회
SELECT TABLE_OWNER, TABLE_NAME, TRIGGER_NAME
  FROM USER_TRIGGERS
 ORDER BY 1,2,3;

PAUSE

REM 권한이 있다면 DBA_TRIGGERS에서 트리거 목록 조회
SELECT TABLE_OWNER, TABLE_NAME, TRIGGER_NAME
  FROM DBA_TRIGGERS
 WHERE TABLE_OWNER = 'SCOTT'
 ORDER BY 1,2,3;

