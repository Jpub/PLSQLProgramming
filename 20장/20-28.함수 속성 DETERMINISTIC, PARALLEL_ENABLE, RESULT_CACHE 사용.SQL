REM 함수 속성 DETERMINISTIC, PARALLEL_ENABLE, RESULT_CACHE 사용
CREATE OR REPLACE FUNCTION factorial(a_num PLS_INTEGER)
RETURN NUMBER
  DETERMINISTIC PARALLEL_ENABLE RESULT_CACHE
IS
BEGIN
  IF a_num <= 1 THEN
    RETURN 1 ;
  ELSE
    RETURN a_num * factorial(a_num-1) ;
  END IF ;
END ;
/
